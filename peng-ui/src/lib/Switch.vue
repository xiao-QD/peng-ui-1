<template>
  <button @click="toggle" :class="{ checked: value }"><span></span></button>
</template>

<script setup lang="ts">
import { ref } from "vue";

const props = defineProps({
  value: Boolean,
});

const emit = defineEmits(["changeShow"]);
const toggle = () => {
  emit("changeShow", !props.value);
};
</script>

<style lang="scss" scoped>
$h: 22px;
$h2: $h - 4px;
button {
  height: $h;
  width: $h * 2;
  border: none;
  background: grey;
  border-radius: calc($h2 / 2);
  position: relative;
}
span {
  position: absolute;
  top: 2px;
  left: 2px;
  height: $h2;
  width: $h2;
  background: white;
  border-radius: calc($h2 / 2);
  transition: left 250ms;
}
button.checked {
  background: blue;
}
button.checked > span {
  left: calc(100% - #{$h2} - 2px);
}
button:focus {
  outline: none;
}
</style>
